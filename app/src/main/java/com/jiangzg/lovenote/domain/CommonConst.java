    private String QQCustomer;
    private String QQGroup;
    private String contactEmail;

    public String getQQCustomer() {
        return QQCustomer;
    }

    public void setQQCustomer(String QQCustomer) {
        this.QQCustomer = QQCustomer;
    }

    public String getQQGroup() {
        return QQGroup;
    }

